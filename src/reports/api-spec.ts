function buildEndpointTable(input: GenerateApiSpecInput): string {
  const rows = input.endpoints.map(
    (e) =>
      `| \`${e.method}\` | \`${e.path}\` | ${escape(e.summary)} | ${e.auth_scheme ?? "bearer_jwt"} | ${e.rate_limit_tier ?? "standard"} | ${e.pagination_strategy} | ${e.requires_idempotency ? "✅" : "—"} | ${e.deprecated ? `⚠️ sunset ${e.sunset_date}` : "—"} |`,
  );
  return [
    "| Method | Path | Summary | Auth | Rate Limit | Pagination | Idempotent | Deprecation |",
    "|---|---|---|---|---|---|---|---|",
    ...rows,
  ].join("\n");
}

function buildConventionsBlock(input: GenerateApiSpecInput): string {
  const stdLimit = TIER_LIMITS.standard;
  const elevLimit = TIER_LIMITS.elevated;
  return [
    "**Authentication.** Default `BearerJWT` (RS256/ES256). `alg: none` rejected. JWT claims `iss`, `aud`, `exp`, `nbf` validated.",
    "",
    "**Idempotency.** All `POST`/`PUT` (and any endpoint with `requires_idempotency=true`) must include `Idempotency-Key: <uuid v4>`. Server stores key→response for 24 h; reuse with a different body yields `422 IDEMPOTENCY_KEY_REUSE`.",
    "",
    "**Errors.** RFC 7807 `application/problem+json` with mandatory `error_code` (UPPER_SNAKE_CASE), `request_id`, `timestamp`.",
    "",
    `**Rate limits.** \`standard\` = ${stdLimit.perMinute}/min (+${stdLimit.burst} burst), \`elevated\` = ${elevLimit.perMinute}/min (+${elevLimit.burst} burst), \`unlimited\` reserved for internal traffic. \`X-RateLimit-Limit\`, \`X-RateLimit-Remaining\`, \`X-RateLimit-Reset\` returned on every response.`,
    "",
    `**Versioning.** URL prefix \`/v${input.version.split(".")[0]}\`. Additive changes do not bump major; breaking changes do. Deprecated endpoints emit \`Deprecation\` and \`Sunset\` headers (≥ 6 months notice).`,
    "",
    "**CORS.** Explicit allowlist origin (no `*` in production). `Authorization`, `Idempotency-Key`, `X-API-Key`, `X-Request-ID` whitelisted.",
  ].join("\n");
}

function buildQualityGateChecklist(input: GenerateApiSpecInput): string {
  const dupes = findDuplicatePaths(input);
  const missingSunset = input.endpoints.filter((e) => e.deprecated && !e.sunset_date);
  const checks: Array<{ ok: boolean; label: string }> = [
    { ok: dupes.length === 0, label: "All `(method, path)` combinations are unique" },
    {
      ok: input.endpoints.every(
        (e) => !(e.method === "POST" || e.method === "PUT") || e.requires_idempotency,
      ),
      label: "Every POST/PUT requires `Idempotency-Key`",
    },
    {
      ok: input.endpoints.every((e) => e.method !== "GET" || e.pagination_strategy !== undefined),
      label: "Every GET declares a `pagination_strategy`",
    },
    { ok: missingSunset.length === 0, label: "Every deprecated endpoint has a `sunset_date`" },
    {
      ok: input.endpoints.every((e) => e.auth_scheme !== "none") || input.endpoints.length === 0,
      label: "No endpoint defaults to unauthenticated unless explicitly intended",
    },
  ];
  return checks.map((c) => `- [${c.ok ? "x" : " "}] ${c.label}`).join("\n");
}

function buildRecommendations(input: GenerateApiSpecInput, risks: RiskEntry[]): string[] {
  const recs: string[] = [];
  if (risks.some((r) => r.id.startsWith("AUTH-"))) {
    recs.push(
      "Document the rationale for any unauthenticated endpoints in an ADR; require security review sign-off.",
    );
  }
  if (risks.some((r) => r.id.startsWith("IDEMP-"))) {
    recs.push(
      "Enable `requires_idempotency=true` on every state-mutating endpoint to make retries safe by default.",
    );
  }
  if (input.endpoints.some((e) => e.pagination_strategy === "offset")) {
    recs.push(
      "Migrate offset pagination to cursor pagination once datasets exceed 10k rows — offset performance degrades at high values.",
    );
  }
  if (!input.endpoints.some((e) => e.deprecated)) {
    recs.push(
      "Define a deprecation policy now (even with no current deprecations) so future sunsets follow the same playbook.",
    );
  }
  recs.push(
    "Lint the generated YAML with `spectral` and the OWASP API Security ruleset before publishing.",
  );
  recs.push("Generate typed SDKs from this spec to keep clients in lockstep with the contract.");
  return recs;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function quote(s: string): string {
  return `"${s.replace(/"/g, '\\"')}"`;
}

function escape(s: string): string {
  return s.replace(/\|/g, "\\|");
}

function operationId(method: string, p: string): string {
  const cleaned = p
    .replace(/^\//, "")
    .replace(/\{([^}]+)\}/g, "By_$1")
    .replace(/\//g, "_")
    .replace(/-/g, "_");
  return `${method.toLowerCase()}_${cleaned || "root"}`;
}

function deriveTag(p: string): string {
  const first = p.split("/").filter(Boolean)[0] ?? "default";
  return first.replace(/\{[^}]+\}/g, "default") || "default";
}

function uniqueTags(input: GenerateApiSpecInput): string[] {
  return Array.from(new Set(input.endpoints.map((e) => deriveTag(e.path)))).sort();
}

function extractPathParams(p: string): string[] {
  return Array.from(p.matchAll(/\{([^}]+)\}/g)).map((m) => m[1]);
}

function authSchemeRef(scheme: string): string {
  switch (scheme) {
    case "oauth2_client_credentials":
      return "OAuth2ClientCredentials";
    case "api_key":
      return "ApiKey";
    case "bearer_jwt":
    default:
      return "BearerJWT";
  }
}

function responseRef(status: string): string {
  return (
    {
      "400": "BadRequest",
      "401": "Unauthenticated",
      "403": "Unauthorized",
      "404": "NotFound",
      "422": "ValidationFailed",
      "429": "RateLimited",
      "503": "ServiceUnavailable",
    }[status] ?? "BadRequest"
  );
}

function findDuplicatePaths(input: GenerateApiSpecInput): string[] {
  const seen = new Set<string>();
  const dupes: string[] = [];
  for (const e of input.endpoints) {
    const key = `${e.method} ${e.path}`;
    if (seen.has(key)) dupes.push(key);
    seen.add(key);
  }
  return dupes;
}

function countByTier(input: GenerateApiSpecInput): Record<string, number> {
  const counts: Record<string, number> = { standard: 0, elevated: 0, unlimited: 0 };
  for (const e of input.endpoints) {
    counts[e.rate_limit_tier ?? "standard"]++;
  }
  return counts;
}
